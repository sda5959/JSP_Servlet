package evaluation;

public class Evaluation {
	private int bbsID;
	private String userID;
	private int good;
	private int soso;
	private int bad;
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getgood() {
		return good;
	}
	public void setgood(int good) {
		this.good = good;
	}
	public int getsoso() {
		return soso;
	}
	public void setsoso(int soso) {
		this.soso = soso;
	}
	public int getbad() {
		return bad;
	}
	public void setbad(int bad) {
		this.bad = bad;
	}


}
